function checkParentNo(Parent,ParentNo){//根据分类名称选择分类序号
	var Region;
	for(i=0; i<Parent.length; i++){
		Region=Parent[i][2];
		if(Region.indexOf(ParentNo)==0)
			return i;
	}
	return -1;
}

function checkParentID(Parent,ParentNo){//根据分类编号选择分类序号
	for(i=0; i<Parent.length; i++){
		if(Parent[i][1]==ParentNo)
			return i;
	}
	return -1;
}

function checkOption(targ,sv){//根据选定值定位下拉菜单


	for(i=0;i<=targ.length;i++){
		if(targ.options[i].value==sv){
			targ.selectedIndex=i;
			break;
		}
	}	
}

function selectRegion(s,t,tf){//定位区域,s:区域名称，t:区域类别,tf:目标表单
	var p,c,u;
	p=-1;	//省份编号
	c=-1;	//城市编号
	u=-1;	//区县编号
	if(t=='ProvinceID'){
		p=checkParentNo(province,s);
	}
	else if(t=='CityID'){
		c=checkParentNo(capital,s);		
		if(c!=-1){
			p=checkParentID(province,capital[c][0]);
		}
	}
	else if(t=='County'){
		u=checkParentNo(city,s);
		if(u!=-1){
			c=checkParentID(capital,city[u][0]);	
		}
		if(c!=-1){
			p=checkParentID(province,capital[c][0]);
		}
	}
	else{
		return;
	}
	if(p!=-1){//定位省份
		checkOption(tf.provinceCN,province[p][1]);
		changeProvinceList(tf.provinceCN,tf.capitalCN,province[p][1]);
	}
	if(c!=-1){//定位城市
		checkOption(tf.capitalCN,capital[c][1]);
		changeCapitalCity(tf.capitalCN,tf.cityCN,capital[c][1]);
	}
	if(u!=-1){//定位区县
		checkOption(tf.cityCN,city[u][1]);
	}
}

